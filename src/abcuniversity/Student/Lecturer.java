/**
 *
 * @author ASUS
 */
public class Lecturer {

    
    private int regId;
    private String firstName;
    private String lastName;
    private int Age;
    private String Address;
    private String Gender;
    private String Faculty;
    private String highestQualification;
    private int yearOfServices;
    
    
    
    
    public int getRegId() {
        return regId;
    }

    /**
     * @param regId the regId to set
     */
    public void setRegId(int regId) {
        this.regId = regId;
    }

    /**
     * @return the firstName
     */
    public String getFirstName() {
        return firstName;
    }

    /**
     * @param firstName the firstName to set
     */
    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    /**
     * @return the lastName
     */
    public String getLastName() {
        return lastName;
    }

    /**
     * @param lastName the lastName to set
     */
    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    /**
     * @return the Age
     */
    public int getAge() {
        return Age;
    }

    /**
     * @param Age the Age to set
     */
    public void setAge(int Age) {
        this.Age = Age;
    }

    /**
     * @return the Address
     */
    public String getAddress() {
        return Address;
    }

    /**
     * @param Address the Address to set
     */
    public void setAddress(String Address) {
        this.Address = Address;
    }

    /**
     * @return the Gender
     */
    public String getGender() {
        return Gender;
    }

    /**
     * @param Gender the Gender to set
     */
    public void setGender(String Gender) {
        this.Gender = Gender;
    }

    /**
     * @return the Faculty
     */
    public String getFaculty() {
        return Faculty;
    }

    /**
     * @param Faculty the Faculty to set
     */
    public void setFaculty(String Faculty) {
        this.Faculty = Faculty;
    }

    /**
     * @return the highestQualification
     */
    public String getHighestQualification() {
        return highestQualification;
    }

    /**
     * @param highestQualification the highestQualification to set
     */
    public void setHighestQualification(String highestQualification) {
        this.highestQualification = highestQualification;
    }

    /**
     * @return the yearOfServices
     */
    public int getYearOfServices() {
        return yearOfServices;
    }

    /**
     * @param yearOfServices the yearOfServices to set
     */
    public void setYearOfServices(int yearOfServices) {
        this.yearOfServices = yearOfServices;
    }
    
    
    
}
